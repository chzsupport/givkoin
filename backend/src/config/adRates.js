const DEFAULT_AD_RATE = 0.10; // Ставка для неопределенных стран (Fallback)

const getAdRate = (countryCode) => {
    if (!countryCode) return DEFAULT_AD_RATE;
    const code = countryCode.toUpperCase();

    for (const tier in TIER_RATES) {
        if (TIER_RATES[tier][code]) {
            return TIER_RATES[tier][code];
        }
    }
    return DEFAULT_AD_RATE;
};

const calculateRevenue = (impressions, adRate) => {
    return (impressions * adRate) / 1000;
};

const getTier = (countryCode) => {
    if (!countryCode) return 'UNKNOWN';
    const code = countryCode.toUpperCase();

    for (const tier in TIER_RATES) {
        if (TIER_RATES[tier][code]) {
            return tier;
        }
    }
    return 'UNKNOWN';
}

